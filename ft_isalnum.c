int	ft_isalnum(int carac)
{
	if (carac <= 57 && carac >= 48)
	{
		return (8);
	}
	else if ((carac >= 97 && carac <= 122) || (carac >= 65 && carac <= 90))
	{
		return (8);
	}
	return (0);
}
